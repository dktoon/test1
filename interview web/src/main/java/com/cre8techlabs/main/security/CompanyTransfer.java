package com.cre8techlabs.main.security;

import com.cre8techlabs.entity.company.Company;

public class CompanyTransfer {
	private String id;
	private String name;
	
	public CompanyTransfer() {
	}
	
	public CompanyTransfer(Company company) {
		this.id = company.getId().toString();
		this.name = company.getName();
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
